    // role JComboBox : manager/customer
    private void role_comboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_role_comboActionPerformed
        // TODO add your handling code here:
        username_txtField.setText(null);
        password_txtField.setText(null);
        email_txtField.setText(null);
        contact_txtField.setText(null);
        gender_group.clearSelection();
        age_txtField.setText(null);
        address_txtField.setText(null);
        DefaultTableModel model = (DefaultTableModel) account_table.getModel();
        while(account_table.getRowCount()!=0){
            model.removeRow(0);
        }
        // open user.txt file
        File file = new File("user.txt");
        try {
            BufferedReader tr = new BufferedReader (new FileReader(file));
            Object[] tableLines = tr.lines().toArray();
            for(int i = 0; i <tableLines.length;i++)
            {
                String line = tableLines[i].toString().trim();
                // if JComboBox was selected, it will shows the details of the role
                if(line.contains((String) role_combo.getSelectedItem())){                
                    String[] dataRow = line.split(",");
                    model.addRow(dataRow);                 
                }
            }
        } catch (FileNotFoundException ex) {
            Logger.getLogger(manage_account.class.getName()).log(Level.SEVERE, null, ex);
        } catch (IOException ex) {
            Logger.getLogger(manage_account.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_role_comboActionPerformed

