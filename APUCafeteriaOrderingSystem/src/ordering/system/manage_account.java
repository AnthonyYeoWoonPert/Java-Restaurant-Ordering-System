    // update button
    private void modify_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_modify_buttonActionPerformed
        // TODO add your handling code here:
        
        // define new variables to update
        String editID = id_txtField.getText();
        String new_gender = null;
        String new_username = username_txtField.getText();
        String new_password = password_txtField.getText();
        String new_email = email_txtField.getText();
        String new_contact = contact_txtField.getText();
        if (male_rbutton.isSelected())
        {
            new_gender = "Male";
        }
        else if (female_rbutton.isSelected())
        {
            new_gender = "Female";
        }
        String new_age = age_txtField.getText();
        String new_address = address_txtField.getText();

        ArrayList<String> tempCustomer = new ArrayList<>();
        try {
            try
            {
                FileReader fr = new FileReader("user.txt");
                Scanner reader = new Scanner (fr);

                String line;
                while((line=reader.nextLine())!=null) {
                    String[] data = line.split(",");
                    // temporarily store in "tempCustomer" array list
                    if (data[0].equals(editID)) {
                        tempCustomer.add(
                            data[0]+","+
                            new_password+","+
                            data[2]+","+
                            new_username+","+
                            new_email+","+
                            new_contact+","+
                            new_gender+","+
                            new_age+","+
                            new_address);

                        JOptionPane.showMessageDialog(this, "Successfully Updated");
                        this.dispose();
                        manageAcc_menu mm = new manageAcc_menu();
                        mm.setVisible(true);
                    }
                    else
                    {
                        tempCustomer.add(line);
                    }
                }
                fr.close();
            }
            catch(Exception e){
                // JOptionPane.showMessageDialog(this, "Error","Error Box",JOptionPane.ERROR_MESSAGE);;
            }
            try {
                // overwrite user.txt file after update the specific row
                PrintWriter fw = new PrintWriter("user.txt");

                for(String str : tempCustomer){
                    fw.println(str);
                }
                fw.close();
            } catch(Exception e){
                // JOptionPane.showMessageDialog(this, "Error","Error Box",JOptionPane.ERROR_MESSAGE);;
            }
        } catch(Exception e) {
            // JOptionPane.showMessageDialog(this, "Error","Error Box",JOptionPane.ERROR_MESSAGE);;
        }

    }//GEN-LAST:event_modify_buttonActionPerformed

    // delete button
    private void delete_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_delete_buttonActionPerformed
        // TODO add your handling code here:
        String deleteID = id_txtField.getText();
                
        ArrayList<String> tempCustomer = new ArrayList<>();
        try {
            try
            {
                FileReader fr = new FileReader("user.txt");
                Scanner reader = new Scanner (fr);

                String line;
                while((line=reader.nextLine())!=null) {
                    String[] data = line.split(",");
                    if (data[0].equals(deleteID)) {
                        JOptionPane.showMessageDialog(this, "Successfully Deleted");
                        this.dispose();
                        manageAcc_menu mm = new manageAcc_menu();
                        mm.setVisible(true);
                    }
                    else
                    {
                        tempCustomer.add(line);
                    }
                }
                fr.close();
            }
            catch(Exception e){
                // JOptionPane.showMessageDialog(this, "Error","Error Box",JOptionPane.ERROR_MESSAGE);;
            }
            // overwrite user.txt file after delete the specific row
            try {
                PrintWriter fw = new PrintWriter("user.txt");

                for(String str : tempCustomer){
                    fw.println(str);
                }
                fw.close();
            } catch(Exception e){
                // JOptionPane.showMessageDialog(this, "Error","Error Box",JOptionPane.ERROR_MESSAGE);;
            }
        } catch(Exception e) {
            // JOptionPane.showMessageDialog(this, "Error","Error Box",JOptionPane.ERROR_MESSAGE);;
        }
    }//GEN-LAST:event_delete_buttonActionPerformed

    // fill in the specific data when the JTable row was selected
    private void account_tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_account_tableMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)account_table.getModel();
        int selectedRowIndex = account_table.getSelectedRow();
        
        String gender = model.getValueAt(selectedRowIndex, 6).toString();
        if (gender.equals("Male")) {
            male_rbutton.setSelected(true);
        }
        else {
            female_rbutton.setSelected(true);
        }
        username_txtField.setText(model.getValueAt(selectedRowIndex, 3).toString());
        password_txtField.setText(model.getValueAt(selectedRowIndex, 1).toString());
        email_txtField.setText(model.getValueAt(selectedRowIndex, 4).toString());
        contact_txtField.setText(model.getValueAt(selectedRowIndex, 5).toString());
        age_txtField.setText(model.getValueAt(selectedRowIndex, 7).toString());
        address_txtField.setText(model.getValueAt(selectedRowIndex, 8).toString());
        id_txtField.setText(model.getValueAt(selectedRowIndex, 0).toString());
        id_txtField.setVisible(false);
    }//GEN-LAST:event_account_tableMouseClicked

