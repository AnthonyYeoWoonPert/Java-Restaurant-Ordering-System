    // submit button
    private void submit_buttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_submit_buttonActionPerformed
        // TODO add your handling code here:
        // define login page as lg
        Login lg = new Login();
        String gender = null;
        try {
            File f = new File("user.txt");
            BufferedWriter bw = new BufferedWriter(new FileWriter(f,true));
            BufferedReader br = new BufferedReader (new FileReader(f));
            if (male_rButton.isSelected()) {
                gender = "Male";
            } else if (female_rButton.isSelected()) {
                gender = "Female";
            }
            // write all information to user.txt
            if (customer_rButton.isSelected()) {
                Customer cs = new Customer(username_txtField.getText(), email_txtField.getText(), contact_txtField.getText(), gender, age_txtField.getText(), confirmpass_txtField.getText(), address_txtField.getText());
                bw.write(cs.getCustomerID()+id_txtField.getText()+","+cs.getPassword()+","+cs.getCustomerRole()+","+cs.getName()+","+cs.getEmail()+","+cs.getContact()+","+cs.getGender()+","+cs.getAge()+","+cs.getAddress()+"\n");
                bw.close();
                JOptionPane.showMessageDialog(this, "Successful Register");
                lg.setVisible(true);
                this.dispose(); 
             }
             else if (manager_rButton.isSelected()) {
                Manager mg = new Manager(username_txtField.getText(), email_txtField.getText(), contact_txtField.getText(), gender, age_txtField.getText(), confirmpass_txtField.getText(), address_txtField.getText());
                bw.write(mg.getManagerID()+id_txtField.getText()+","+mg.getPassword()+","+mg.getManagerRole()+","+mg.getName()+","+mg.getEmail()+","+mg.getContact()+","+mg.getGender()+","+mg.getAge()+","+mg.getAddress()+"\n");
                bw.close();
                JOptionPane.showMessageDialog(this, "Successful Register");
                lg.setVisible(true);
                this.dispose(); 
             }
            
        } catch (IOException e) {
            JOptionPane.showMessageDialog(this, "Error","Error Box",JOptionPane.ERROR_MESSAGE);;
        }
    }//GEN-LAST:event_submit_buttonActionPerformed

