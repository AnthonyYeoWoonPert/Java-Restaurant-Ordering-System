    // id validation
    private void id_txtFieldKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_id_txtFieldKeyReleased
        // TODO add your handling code here:

        String line;
        File export_file = new File("user.txt");
        try
        {
            submit_button.setEnabled(true);
            String PATTERN="^[0-9]{3}[1-9]$";
            Pattern patt = Pattern.compile(PATTERN);
            Matcher match=patt.matcher(id_txtField.getText());
            BufferedReader br = new BufferedReader(new FileReader(export_file));
            while ((line = br.readLine()) != null) {
                String userData [] = line.split(",");
                if (customer_rButton.isSelected()) {
                    if(!match.matches()) {
                        nlab7.setText("It must be integer with 4 digit");
                        submit_button.setEnabled(false);
                    }
                    else if (userData[0].equals("C"+id_txtField.getText())) {
                        nlab7.setText("Customer ID Existed");
                        submit_button.setEnabled(false);
                        }
                    else {
                        nlab7.setText(null);
                        submit_button.setEnabled(true);
                    }
                }
                else if (manager_rButton.isSelected()) {
                    if(!match.matches()) {
                        nlab7.setText("It must be integer with 4 digit");
                        submit_button.setEnabled(false);
                    }
                    else if (userData[0].equals("M"+id_txtField.getText())) {
                        nlab7.setText("Manager ID Existed");
                        submit_button.setEnabled(false);
                        }
                    else {
                        nlab7.setText(null);
                        submit_button.setEnabled(true);
                    }
                }
            }
        }catch(IOException Ex){

        }
    }//GEN-LAST:event_id_txtFieldKeyReleased

